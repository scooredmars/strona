var a = parseInt(prompt("Podaj  liczbe: "));

if (a > 0) {
  document.write("Liczba: " + a + " jest wieksza od 0 </br>")
} else {
  document.write("Liczba: " + a + " nie jest wieksza od 0 </br>")
}
